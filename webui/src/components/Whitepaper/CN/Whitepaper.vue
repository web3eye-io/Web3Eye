      <h3>4.2  检索结果</h3>
      <p>
        Web3.0 Eye 的主要目标之一，在于为用户提供结构化的 NFT 检索结果，以便于用户可以清 楚了解自己所关心的内容在 Web3.0 网络的详细轨迹。当用户输入检索内容，执行检索， 
        Web3.0 Eye 检索引擎执行向量检索与布尔检索结合的混合检索算法，将相应检索结果返回给 客户端展示。客户端将数据按照时间序列排列，并将各相关内容与原始检索内容的相似度关系、
        是否同一作者上传等信息展示给用户。此外，对于检索相关内容的用户，我们预期用户 已经对该内容产生了兴趣，因此，Web3.0 Eye 客户端将组织好的检索目标交易信息一并展示， 
        便于用户了解检索目标的近期交易历史。与首页的“随便逛逛”相似，Web3.0 Eye 客户端亦 将聚合的内容按照一定指标排序，在检索结果页作为快捷访问入口，方便用户访问 Web3.0 网络上出现的新内容或已有内容的新动向。
