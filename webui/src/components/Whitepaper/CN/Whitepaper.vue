        在 Web3.0 Eye 的构 思中，使用者只需要提供一张图，该图可能来自于其购买的 NFT，也可能是使用者创作的 NFT 作品，Web3.0 Eye 将整合与该图相关的 Web2.0 和 Web3.0 关联内容，
        按照时间线返回 给使用者。对于购买者，其价值在于为购买者提供购买对象的历史线索以及与购买对象相似 的作品；对于创作者，其价值在于让创作者了解自己的作品是否与历史上曾出现的作品雷同； 
