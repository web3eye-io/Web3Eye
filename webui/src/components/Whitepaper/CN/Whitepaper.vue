      <p>
        传统的布尔检索技术发展到一定阶段，网络上非结构化数据逐渐增多，布尔检索技术已经很难高效检索。基于机器学习的向量检索技术将检索目标向量化（Embedding），将物品使用n维属性向量表达，
        为待检索物建立统一空间映射，由此，当用户需要检索某物，只需要将该物件向量化后，针对向量化后的值进行检索，即可从海量数据中快速召回所需结果。当然，对于海量数据的检索并非上述几句话可以简单概括，
        我们在此简要说明向量检索技术，仅为了描述Web3.0 Eye达成目标需要的技术与学术基础。但在Web3.0领域，向量检索技术亦存在其局限性，例如：套装的NFT通常都是相同的图辅以不同特征，
        从而形成独一无二的作品，但在向量检索结果集中，套装的NFT可能会被识别为同一作品。随着Web3.0技术的发展，我们相信普通用户最终亦有能力在Web3.0发表作品，画家可以将自己的画作Mint为独一无二的NFT，
        作家可以将自己的作品上传到IPFS网络，然后将其Token化。因此，一方面我们将基于Milvus持续研发适合当下套装NFT场景的检索方法，另一方面，我们亦将开拓向量检索在将来的Web3.0内容场景的检索应用。
