      <h2>3  技术分析</h2>
      <p>何谓数据能力？聚合、检索、分析、传输即为数据能力。</p>
      <p>
        Web3.0 的进化过程始于区块链，NFT 的发行都依赖于特定公链，不同公链之间不能互通， 导致在不同的公链之间形成数据孤岛。当人们想检索持有的作品，需要在不同公链查询，
        获取该作品在不同公链上的发行情况。这一过程存在几个困难：其一，查询者需要熟悉不同公 链的内容查询方式；其二，查询者需要自己整合查询结果；其三，由于当前区块链查询较为原始，
        查询者需要自己总结作品特征进行文本查询。
      </p>
      <p>对于 NFT 作品的查询以及结果呈现，是否有更加便捷的方式？</p>
      <p>
        自 2009 年 google 搜图上线以来，经历 AI 大爆炸，基于向量检索的以图搜图技术趋于成熟。 NFT 本质上是一个部署在公链上的智能合约，以及存储在链下的媒体内容，链上的智能合约
        储存了媒体内容的存放位置。对于当前的 NFT 而言，该内容大部分为图片，存储在 IPFS 或 中心化的云存储，后将 IPFS 的文件 CID 或中心化存储的 URL 记录在智能合约。NFT 应用从智 能合约获取
        NFT 的存储位置，再通过该位置将 NFT 内容下载后渲染呈现。当前市场上对于 NFT 数据的处理，大体上可分为两种：其一，从智能合约获取 NFT 并呈现，其代表为 OpenSea， 主要关注于 NFT 的展示，
        但缺乏跨链检索能力；其二，以合约 ID 从链上检索 NFT，并将支 持公链的同类 NFT 一起呈现，其代表为 center.dev，主要关注于通过合约 ID 检索已经存在的 NFT，使用方式更倾向于为应用提供 NFT 检索
        但该检索能力不能面向最终用户。我们认为 NFT 作品的检索应该可以有更加简洁的方式，和以图搜图的结合，可以让用户的 NFT 检索过 程便捷而流畅，在检索结果可以更加有效整合不同维度的作品信息
        并可以整合 Web2.0 的 同类数据，提供更加深度的检索结果。
      </p>
      <h3>3.1  聚合</h3>
      <p>
        当今日头条推出算法推荐，将人们隔离在自我认知的信息孤岛，我们会忘记今日头条事实上 是一个信息聚合软件，披着算法推荐和信息流的外衣。今天对于蓬勃出现的各种公链，以及 依附于其上的 NFT 内容，
        我们至少需要一个公链的聚合软件，让人们有更加便捷的方式可 以访问不同生态的 NFT 作品。这样的聚合能力一方面丰富了 NFT 的生态，另一方面使得发 行在不同公链的内容拥有门槛更低的流量入口，
        为创作者带来更多展示机会。
      </p>
      <h3>3.2  分析</h3>
      <p>
        我们之所以将分析列于检索之前，在于我们认为无论是传统互联网，亦或是 Web3.0 内容网 络，内容都已泛滥成灾，我们无需担心这一网络中没有用户需要的内容，随着时间发展，网 络规模以及参与者规模增长，
        内容将会爆炸式增长，我们需要关注的是为合适的人提供合适 的内容。有人关心格调，有人关心价钱，有人为了收藏，有人为了投机，目的不同者各取所 需。今日头条依靠其算法推荐引擎，为用户提供源源不断的领域相关内容，
        但也因此将用户 隔离在自我认知的信息孤岛，甚至增加用户获取不同领域信息的壁垒。Web2.0 的互联网巨 头通过搜集个人行为信息为用户生成标签，以实现不同用户的内容定位。Web3.0 注重用户隐私，
        注重用户数据所有权，我们或许可以从数据出发，为使用者提供基于内容的更有深度 的分析结果。相较于 Web2.0 的数据分析引擎而言，Web3.0 的分析引擎更加关注于挖掘数据自身的价值，
        将更加广泛的更有价值的内容呈现给用户，而非挖掘用户习惯以便于给用户推 送更多相同领域的重复信息。
      </p>
      <h3>3.3  检索</h3>
      <p>
        从最早的图书馆编目，到图书馆管理系统，到门户网站超级链接，到索引全网数据的搜索引 擎，到基于用户行为习惯的千人千面，信息一直以最原始亦最高效的方式组织。
        今天我们从 google 搜索到一张图片，一篇文章，其获取的结果与此前我们到图书馆，查询一本书的位置， 找到该书并获取某一章节的内容并无二致。检索是与信息的产生相伴生的能力，
        但往往在内容增长到相当规模，信息检索才更加重要。于检索而言，其方式总是从最粗暴的信息聚合开 始，逐渐向更加精准的搜索结果演化。我们认为，Web3.0 最终在内容检索上，
        亦需要经历 与其前辈相同的检索方式演进，今天我们甚至不能判断传统的搜索引擎是否是将来 Web3.0 的检索方式，但无可否认的是高效的信息组织方式以及信息检索一定是其不可或缺的能力。
      </p>
      <h3>3.4  传输</h3>
      <p>
        无论是传统互联网的内容提供商，还是基于区块链的内容提供商，都需要将数据从内容提供 方传输到内容客户端，其区别仅在于信源是来自于某中心化组织托管的服务器，还是来自于运行区块链系统的某节点。
        运行于中心化组织托管的服务器依靠内容提供方确保内容在线， 区块链节点则依靠分布于世界各地的节点存储的副本数确保内容安全。中心化服务器可以使 用先进的传输技术如 SDN、QUIC 等网络优化手段进一步提升传输效率，
        优化客户端使用体 验，区块链节点只能依靠点对点传输提供数据，而该点对点传输通常架设于公用网络之上， 其效率依赖于公用网络路由选择的合理性。但区块链节点的点对点传输或许能够在应用层架设更加灵活的多径传输通道，
        这亦是中心化内容传输所不能比拟。总而言之，Web3.0 上的 使用体验，将来亦极大依赖于高效的内容传输能力。我们不能想象，当用户试图获取其 NFT 作品，
        该作品却需要从遥远的美利坚合众国经由 IPFS 的点对点网络拉取到南亚次大陆，该 过程让用户为了一张 3M 的图片等待了 30 秒钟，这在传统互联网是一种怎样灾难的体验。
      </p>
      <p class='caption'>
        除此之外，数据能力还包含经典的存储、分发、缓存、持久化、恢复、安全等能力，但这些能力是所有涉 及数据服务的从业者都需要面对的基础，无论是 Web2.0 的从业者还是 Web3.0 的从业者。
        我们特别列举聚 合、分析、检索、传输几种能力，是特定于基于区块链与分布式存储系统的 Web3.0 场景下，达成良好的应 用体验需要克服的挑战。
      </p>
      <h2>4  产品呈现</h2>
      <p>
        关于 Web3.0 的聚合与检索产品形态，我们在市场上找到的面向终端用户的产品，不外乎于 各公链的浏览器、钱包，以及 NFT 的交易所，这些产品更多偏向于特定于某公链的内容， 
        我们并未找到可以聚合全部公链的产品。检索方面，The Graph 试图构建跨链的 Web3.0 索 引协议，使得第三方开发者可以对不同公链构建检索服务，但 The Graph 并非面向终端用户 的产品；
        center.dev 提供按照合约 ID 检索关联 NFT 的服务，但合约 ID 是使用者已经清楚内 容与公链的关系之后才存在的资料。我们认为，Web3.0 的内容领域也应该存在与 Web2.0 领域相似的内容检索方式，
        无论是基于传统的内容匹配的布尔检索，还是基于特征的向量检 索，其检索方式一定不会局限于使用已知的链上资料检索链上的内容，这样的检索是各公链的浏览器需要完成的任务，
        但不是一个面向用户的检索引擎应该完成的任务。Web3.0 Eye 的产品构思，在于提供最简洁的内容检索方式，用户可以通过分结构化的图片检索结构化的 NFT 数据，
        也可以通过合约 ID，图片特征，作者等结构化数据检索 NFT 结果。从用户层面来 说，用户不需要提前了解特定于 Web3.0 相关的信息，只需要提供其感兴趣的内容，即可检 索与之有关的结果。
      </p>
