        然而，Web3.0的内容相较于Web2.0内容有其特殊性，例如，传统的检索引擎将认为两只猴子是相似的图片，其结果即所有无聊猿都具有极高相似度；对于像素人头像，传统的检索引擎将认为所有像素人都是同一作品。
